public class Date {

    public static void main(String[] args) {
        String day = "Friday";
        Int date = 26;
        String month = "August";
        Int year = 2022;
        System.out.print("American format: ");
        System.out.print(day + ", ");
        System.out.print(month + " ");
        System.out.print(date + ", ");
        System.out.println(year);

        System.out.print("European format: ");
        System.out.print(day + " ");
        System.out.print(date + " ");
        System.out.print(month + " ");
        System.out.println(year);
    }
}
